// Global variables for pagination
let currentPage = 1;
const moviesPerPage = 10;
let filteredMovies = [...movies];

// Utility functions to update the UI
function displayMovies(movies) {
  const movieGrid = document.getElementById('movieGrid');
  movieGrid.innerHTML = ''; // Clear the grid

  movies.forEach(movie => {
    const movieCard = document.createElement('div');
    movieCard.classList.add('movie-card');
    movieCard.innerHTML = `
      <img src="${movie.thumbnail_url}" alt="${movie.title}">
      <h3>${movie.title}</h3>
      <p>Rating: ${movie.rating}</p>
      <p>Release Date: ${movie.release_date}</p>
    `;
    movieCard.addEventListener('click', () => showMovieDetails(movie));
    movieGrid.appendChild(movieCard);
  });

  updatePagination();
}

function updatePagination() {
  const totalPages = Math.ceil(filteredMovies.length / moviesPerPage);
  document.getElementById('prevPage').disabled = currentPage === 1;
  document.getElementById('nextPage').disabled = currentPage === totalPages;
}

function showMovieDetails(movie) {
  document.getElementById('movieTitle').innerText = movie.title;
  document.getElementById('movieDirector').innerText = movie.director;
  document.getElementById('movieCast').innerText = movie.cast.join(', ');
  document.getElementById('movieDuration').innerText = movie.duration;
  document.getElementById('movieReleaseDate').innerText = movie.release_date;
  document.getElementById('movieRating').innerText = movie.rating;
  document.getElementById('movieTags').innerText = movie.tags.join(', ');

  document.getElementById('movieModal').style.display = 'flex';
}

document.getElementById('closeModal').addEventListener('click', () => {
  document.getElementById('movieModal').style.display = 'none';
});

document.getElementById('nextPage').addEventListener('click', () => {
  currentPage++;
  updateMovieList();
});

document.getElementById('prevPage').addEventListener('click', () => {
  currentPage--;
  updateMovieList();
});

document.getElementById('search').addEventListener('input', (e) => {
  const query = e.target.value.toLowerCase();
  filteredMovies = movies.filter(movie => 
    movie.title.toLowerCase().includes(query) || 
    movie.genre.toLowerCase().includes(query) ||
    movie.tags.some(tag => tag.toLowerCase().includes(query))
  );
  currentPage = 1;
  updateMovieList();
});

document.getElementById('genreFilter').addEventListener('change', (e) => {
  const genre = e.target.value;
  if (genre) {
    filteredMovies = movies.filter(movie => movie.genre === genre);
  } else {
    filteredMovies = [...movies];
  }
  currentPage = 1;
  updateMovieList();
});

document.getElementById('sortRating').addEventListener('click', () => {
  filteredMovies.sort((a, b) => b.rating - a.rating);
  updateMovieList();
});

document.getElementById('sortReleaseDate').addEventListener('click', () => {
  filteredMovies.sort((a, b) => new Date(b.release_date) - new Date(a.release_date));
  updateMovieList();
});

function updateMovieList() {
  const startIndex = (currentPage - 1) * moviesPerPage;
  const endIndex = startIndex + moviesPerPage;
  const moviesToDisplay = filteredMovies.slice(startIndex, endIndex);
  displayMovies(moviesToDisplay);
}

// Initial Load
updateMovieList();